#!/usr/bin/env node
/**
 * Writes `dist/index.cjs` — the production entrypoint that Replit Autoscale
 * invokes via `node ./dist/index.cjs` after the `npm run build` step.
 *
 * Why this shim exists
 * --------------------
 * Every workspace package under `@pryzm/*` declares `"main": "./src/index.ts"`
 * and ships TypeScript source only (no per-package `dist/`). `server.js` does
 * `import { ... } from '@pryzm/file-format'` etc., so booting it with bare
 * `node` crashes with `ERR_UNKNOWN_FILE_EXTENSION ".ts"`.
 *
 * Until Phase H lands per-package `tsc -b` outputs (tracked in
 * `docs/archive/pryzm3-internal/00_NEW_ARCHITECTURE/phases/audits/PHASE-H-PER-PACKAGE-COMPILE.md`),
 * we boot production through the same `tsx` ESM loader the dev script uses.
 * `tsx` is a runtime dep (see root package.json), so it is present in the
 * deployed image.
 *
 * Trade-off (acknowledged): adds ~150–300 ms of cold-boot transpilation and
 * ~9 MB to the prod image. Phase H removes both costs.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');
const outDir = resolve(repoRoot, 'dist');
const outFile = resolve(outDir, 'index.cjs');

const shim = `// AUTO-GENERATED by scripts/write-prod-shim.mjs — do not edit by hand.
// Boots server.js through the tsx ESM loader so workspace packages that
// expose "main": "./src/index.ts" can be imported in production.
const { spawn } = require("child_process");
const { join } = require("path");
const child = spawn(
  process.execPath,
  ["--import", "tsx", join(__dirname, "..", "server.js")],
  { stdio: "inherit", env: process.env }
);
child.on("exit", (code, signal) => {
  if (signal) process.kill(process.pid, signal);
  else process.exit(code ?? 0);
});
process.on("SIGTERM", () => child.kill("SIGTERM"));
process.on("SIGINT",  () => child.kill("SIGINT"));
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, shim);
console.log(`[write-prod-shim] wrote ${outFile} (${shim.length} bytes)`);
